    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed

        //Si no completa el campo 'ID Usuario'
        if (jtfIDUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe completar el campo 'ID Usuario'");

        } else {
            try {
                //Asignamos a una variable el dato ingresado en la vista
                int idUsuarioBuscado = Integer.parseInt(jtfIDUsuario.getText());

                //Instanciamos un usuario y usuarioData para usar luego
                UsuarioData usuarioD = new UsuarioData();
                Usuario usuarioBuscado = new Usuario();

                //Buscamos un usuario por su id usando buscar de usuarioData
                usuarioBuscado = usuarioD.buscarUsuarioPorID(idUsuarioBuscado);

                //Mostramos en la vista los datos del usuario encontrado excepto administrador (ya que s√≥lo se considera al usuario 'admin'
                jtfIDUsuario.setText(Integer.toString(usuarioBuscado.getId_usuario()));
                jtfNombre.setText(usuarioBuscado.getNombre());
                jtaDNI.setText(Integer.toString(usuarioBuscado.getDni()));
                
                //Habilitamos los botones 'Limpiar' y 'Eliminar'
                jbLimpiar.setEnabled(true);
                jbEliminar.setEnabled(true);

            } catch (NumberFormatException nfe) {
                //si ingresa letras o s√≠mbolos
                JOptionPane.showMessageDialog(this, "Ingrese s√≥lo n√∫meros");
                jtfIDUsuario.setText("");
            } catch (NullPointerException npe) {
                JOptionPane.showMessageDialog(this,npe.getStackTrace());
            }
        }
    }//GEN-LAST:event_jbBuscarActionPerformed

