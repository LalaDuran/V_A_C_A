    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        //Si el campo ID Usuario est√° vac√≠o
        if (jtfIDUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Complete el campo 'ID Usuario'");

        } else {
            try {
                 //Creamos una variable con el ID tipeado en la vista
                int IDUsuarioAEliminar = Integer.parseInt(jtfIDUsuario.getText());
                
                //Instanciamos usuario y usuarioData para usar luego
                Usuario usuario = new Usuario();
                UsuarioData usuarioD = new UsuarioData();

                //Buscamos el usuario con ese id y lo enviamos al usuario ya creado
                usuario = usuarioD.buscarUsuarioPorID(IDUsuarioAEliminar);

                //Eliminamos el usuario llamando al m√©todo de usuarioData
                usuarioD.eliminarUsuario(usuario.getId_usuario());

                //Limpiamos los campos de la vista
                jtfIDUsuario.setText("");
                jtfNombre.setText("");
                jtaDNI.setText("");
                

            } catch (NumberFormatException ex) {
                //Si no tipea un documento en la vista
                JOptionPane.showMessageDialog(null, "Ingrese un ID Usuario");
            }
        }
    }//GEN-LAST:event_jbEliminarActionPerformed

    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
         //Instanciamos usuarioData para usar luego
        UsuarioData usuarioD = new UsuarioData();

        if (jtfIDUsuario.getText().isEmpty()) {
            jtfIDUsuario.setText("-1");
        }

        try {
            //creamos las variables y asignamos los valores de la vista
            String nombreAGuardar = jtfNombre.getText();
            int dniAGuardar = Integer.parseInt(jtaDNI.getText());
            boolean administradorAGuardar = false;
            
            //Instanciamos un usuario con los par√°metros anteriores
            Usuario usuarioAGuardar = new Usuario(nombreAGuardar, dniAGuardar, administradorAGuardar);

            //declaramos una variable bandera por si ya existe el id tipeado en vista
            boolean existeID = false;

            //Recorremos la lista de usuarios existentes
            for (Usuario existingUsuario : usuarioD.listarUsuario()) {

                if (existingUsuario.getId_usuario() == Integer.parseInt(jtfIDUsuario.getText())) {
                    //Si existe el usuario, seteamos el id para poder acceder al m√©todo modificar; si no existe se activa la bandera m√°s abajo 
                    usuarioAGuardar.setId_usuario(usuarioD.buscarUsuarioPorID(Integer.parseInt(jtfIDUsuario.getText())).getId_usuario());
                    existeID = true;
                    break;
                }
            }
            //Si existe el usuario usa el m√©todo modificar; si no, guardar
            if (existeID == true) {
                usuarioD.modificarUsuario(usuarioAGuardar);
            } else {
                usuarioD.guardarUsuario(usuarioAGuardar);
            }

            //Habilitamos los botones 'Eliminar' y 'Limpiar'
            jbEliminar.setEnabled(true);
            jbLimpiar.setEnabled(true);
            
            //Limpiamos los campos de la vista
            jtfIDUsuario.setText("");
            jtfNombre.setText("");
            jtaDNI.setText("");
            

        } catch (NullPointerException ex) {
            //Si alg√∫n campo est√° vac√≠o
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        } catch (NumberFormatException ex) {
            //Si no usa n√∫meros enteros en todos los campos
            JOptionPane.showMessageDialog(null, "Use s√≥lo n√∫meros enteros");
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

